#to convert to integer
number= list(map(int,input("Enter the numbers: ").split()))
print(number)

#maxsplit
number= list(map(int,input("Enter the numbers: ").split(maxsplit=2)))
print(number)

#separator
number= list(map(int,input("Enter the numbers: ").split(sep=",")))
print(number)

